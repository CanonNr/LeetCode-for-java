    public static int chalkReplacer(int[] chalk, int k) {
        int index = 0;
        while(k>0){
            k-=chalk[index];
            if(k<0){
                return index;
            }
            index = next(chalk.length,index);
            if(k==0){
                return index;
            }

        }
        return index;
    }

    public static int next(int chalkLength,int index){
        return index == chalkLength-1 ? 0 : index+1;
    }

